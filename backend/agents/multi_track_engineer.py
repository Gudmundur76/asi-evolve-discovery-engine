"""Multi-Track Engineer for HIV-1 Protease parallel discovery.

Implements four parallel discovery tracks, each seeding from a different
data source to ensure structural diversity. Molecules appearing in multiple
tracks are flagged as convergence candidates.

Track A — ChEMBL Top Actives
    Seeds from the highest-activity ChEMBL bioassay records.
    Explores hydroxyethylamine (HEA) scaffold modifications.

Track B — PDB Co-Crystal Ligands
    Seeds from experimentally validated PDB co-crystal structures.
    Structure-guided modifications of P2/P2' groups.

Track C — BindingDB Curated
    Seeds from BindingDB curated HIV protease records.
    Explores bis-THF and carbamate variants.

Track D — Diverse Scaffolds
    Fragment-based and macrocyclic exploration.
    Targets novel scaffold families with low Tanimoto similarity to
    approved drugs. Uses guided bit-flip mutation from the cognition store.
"""
